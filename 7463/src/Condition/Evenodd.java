package Condition;

public class Evenodd {
	
		public static void main(String[] args) {   
			int n= 45;     
			System.out.println("The given number is " +n);   
			if(n % 2 == 0)      
				System.out.println("The number " +n + " is an even number");    
			else       
				System.out.println("The number " +n + " is an odd number");  
			}
	}
